class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
        set<int> s ;
        vector<Node*> v ;
        Node *a = head ;
        int i;
        while(a)
        {
            if(s.find(a->data)!=s.end());
            else
            {
                v.push_back(a);
                s.insert(a->data);
            }
            a = a->next ;
        }
        for(i = 0 ; i<v.size()-1;i++)
        {
            v[i]->next = v[i+1];
        }
        v[i]->next = NULL;
        return head ;
    }
};


